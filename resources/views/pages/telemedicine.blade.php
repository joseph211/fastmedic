            <p>Telehealth is the distribution of health-related services and information via electronic information and telecommunication technologies. It allows long-distance patient and clinician contact, care, advice, reminders, education, intervention, monitoring, and remote admissions. Telemedicine is sometimes used as a synonym, or is used in a more limited sense to describe remote clinical services, such as diagnosis and monitoring. When rural settings, lack of transport, a lack of mobility, decreased funding, or a lack of staff restrict access to care, telehealth may bridge the gap</p>
            <hr>
            <p>Telehealth could include two clinicians discussing a case over video conference; a robotic surgery occurring through remote access; physical therapy done via digital monitoring instruments, live feed and application combinations; tests being forwarded between facilities for interpretation by a higher specialist; home monitoring through continuous sending of patient health data; client to practitioner online conference; or even videophone interpretation during a consult</p>
            <blockquote>
              <p>Telehealth is sometimes discussed interchangeably with telemedicine. The Health Resources and Services Administration distinguishes telehealth from telemedicine in its scope, defining telemedicine only as describing remote clinical services, such as diagnosis and monitoring, while telehealth includes preventative, promotive, and curative care delivery.[1] This includes the above-mentioned non-clinical applications, like administration and provider education</p>
            </blockquote>
          
            <h2>Methods and modalities</h2>
            <p>Telehealth requires a strong, reliable broadband connection. As broadband infrastructure has improved, telehealth usage has become more widely feasible.

Healthcare providers often begin telehealth with a needs assessment which assesses hardships which can be improved by telehealth such as travel time, costs or time off work</p>
            <h3>Store and forward</h3>
            <p>Store-and-forward telemedicine involves acquiring medical data (like medical images, biosignals etc.) and then transmitting this data to a doctor or medical specialist at a convenient time for assessment offline</p>
           
           
            <h3>Remote monitoring</h3>
            <p>Remote monitoring, also known as self-monitoring or testing, enables medical professionals to monitor a patient remotely using various technological devices. This method is primarily used for managing chronic diseases or specific conditions, such as heart disease, diabetes mellitus, or asthma.</p>
         
